package testpackage;

public class TypesOfOperators {

	public static void main(String[] args) {
		//increment unary operator
		int a = 10;
			System.out.println(++a); //first increment value by 1 then print 
			System.out.println(a);
			System.out.println(a++); //first print value then increment value by 1
			
			//decrement unary operator
			int b=20;
			System.out.println(b++);//first print b value then make decrement
			System.out.println(b);
			System.out.println(--b);//first decrease value by 1 then print in console
	
	int num=1;
			for (int i=num;i<=100;i++) {
			System.out.println(i);
			}
	
	System.out.println("##########################################");
	
	int num1=100;
	for (int j=num1;j>=1;j--) {
	System.out.println(j);
	}
	
	
	}


}



			
