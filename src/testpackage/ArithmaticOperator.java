package testpackage;

public class ArithmaticOperator {

	public static void main(String[] args) {
		
		float a =35, b=10;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		System.out.println("--------------------------------------------------------");
		
		//relational operator
		int s=10, p=25;
		System.out.println(s>p);
		System.out.println(s<p);
		System.out.println(s<=p);
		System.out.println(s>=p);
		System.out.println(s==p);
		System.out.println(s!=p);
		
				
	}

}
